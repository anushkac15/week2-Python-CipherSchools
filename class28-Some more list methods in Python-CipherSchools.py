# count
fruits = ['orange', 'apple', 'pear', 'banana', 'kiwi', 'apple', 'banana', 'mango']
print(fruits.count('apple'))
print(fruits.count('mango'))

# sort method
fruits = ['orange', 'apple', 'pear', 'banana', 'kiwi', 'apple', 'banana', 'mango']
fruits.sort()
print(fruits)

numbers = [3,5,1,9,10]
numbers.sort()
print(numbers)

# sorted function
numbers = [3,5,1,9,10]
print(sorted(numbers))
print(numbers)

# clear
numbers = [3,5,1,9,10]
numbers.clear()
print(numbers)

